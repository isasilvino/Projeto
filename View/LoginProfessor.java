    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        try {

            String nome;
            String senha;

            nome = txtUsuario.getText();
            senha = txtSenha.getText();

            ProfessorDTO objProfessorDTO = new ProfessorDTO();
            objProfessorDTO.setDominio(nome);
            objProfessorDTO.setSenha(senha);            
            ProfessorDAO objProfessorDAO = new ProfessorDAO();
            ResultSet rsProfessorDAO = objProfessorDAO.autenticacaoUsuario(objProfessorDTO);
            
            if (rsProfessorDAO.next()) {
                //chamar tela que eu quero abrir
                ListaDeAlunos objListaView = new ListaDeAlunos();
                objListaView.setVisible(true);
                
                dispose();
                
            } else {
                //enviar mensagem dizendo incorreto
                JOptionPane.showMessageDialog(null, "Usuario ou senha invalida");
            }

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "LOGIN PROFESSOR VIEW" + erro);
        }


    }//GEN-LAST:event_btnEntrarActionPerformed

